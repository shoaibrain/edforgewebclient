/**
 * Get student by ID
 */
export function getStudentById(studentId: string): StudentProfile | undefined {
	return MOCK_STUDENTS.find((student) => student.studentId === studentId);
}

/**
 * Get all students
 */
export function getAllStudents(): StudentProfile[] {
	return MOCK_STUDENTS;
}

/**
 * Filter students based on criteria
 */
export function filterStudents(filters: {
	grade?: string;
	status?: string;
	search?: string;
}): StudentProfile[] {
	return MOCK_STUDENTS.filter((student) => {
		if (filters.grade && student.grade !== filters.grade) {
			return false;
		}
		if (filters.status && student.status !== filters.status) {
			return false;
		}
		if (filters.search) {
			const searchTerm = filters.search.toLowerCase();
			const fullName = `${student.firstName} ${student.lastName}`.toLowerCase();
			return fullName.includes(searchTerm) || student.studentId.toLowerCase().includes(searchTerm);
		}
		return true;
	});
}
